N = int(input())

if N >= 380 and N < 425:
    print("Violet")
elif N >= 425 and N < 450:
    print("Indigo")
elif N >= 450 and N < 495:
    print("Blue")
elif N >= 495 and N < 570:
    print("Green")
elif N >= 570 and N < 590:
    print("Yellow")
elif N >= 590 and N < 620:
    print("Orange")
else:
    print("Red")